export function add(number1, number2){
    var result = number1 + number2;
    return result;
}

export function sub(number1, number2){
    var result = number1 - number2;
    return result;
}

export function mul(number1, number2){
    var result = number1 * number2;
    return result;
}

export function div(number1, number2){
    var result = number1 / number2;
    return result;
}
